export const getMimesByExtensions = function(extensions) {
    if (typeof extensions === 'string') {
        extensions = extensions.split(',')
    }
    extensions.map((extension) => extension.trim())
    let mimes = MIMES.filter((mime) => mime.extension in extensions)
    return mimes
}

export const getMimesByMimes = function(mimes) {
    if (typeof mimes === 'string') {
        mimes = mimes.split(',')
    }
    mimes.map((mime) => mime.trim())
    mimes = MIMES.filter(function(mime) {
        return mime.mime in mimes || mime.mime.split('/')[0] + '/*' in mimes
    })
    return mimes
}
